import { ApplicationCommandOptionType } from "discord.js";

export default {
    name: 'emit',
    category: 'admin',
    permissions: ['ADMINISTRATOR'],
    ownerOnly: true,
    description: 'Emit an event!',
    options: [
        {
            name: 'event',
            description: 'Choose a event!',
            type: ApplicationCommandOptionType.String,
            required: true,
        },
    ],

    async runInteraction(client, interaction) {
        const iEvent = interaction.options.getString('event');
        if(!eventList.find(e => e === iEvent)) return interaction.reply('```Error encountered.. sender is bad```');
        client.emit(iEvent); 
        interaction.reply(`\`\`\`${iEvent} has been issued\`\`\``);
    }
};

